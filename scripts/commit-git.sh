#!/bin/bash

# ==============================================================================
# Git 代码提交脚本
# 使用方式：bash commit-git.sh "提交说明"
# 示例：bash commit-git.sh "修复登录页面样式问题"
# ==============================================================================

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# 配置
PROJECT_DIR="/Users/vincentmiao/Documents/trae_projects/members/membership-system"
BRANCH="main"

echo -e "\n${BLUE}===============================================${NC}"
echo -e "${BLUE}            Git 代码提交脚本${NC}"
echo -e "${BLUE}===============================================${NC}"
echo -e "$(date '+%Y-%m-%d %H:%M:%S')"

# 检查参数
if [ -z "$1" ]; then
    echo -e "${RED}✗ 请提供提交说明！${NC}"
    echo -e "${YELLOW}使用方式: bash commit-git.sh \"提交说明\"${NC}"
    exit 1
fi

COMMIT_MESSAGE="$1"

# ------------------------------------------------------------------------------
# 步骤1: 进入项目目录
# ------------------------------------------------------------------------------
echo -e "\n${YELLOW}【步骤1】进入项目目录...${NC}"
cd "${PROJECT_DIR}"
echo -e "    ${GREEN}✓ 当前目录: $(pwd)${NC}"

# ------------------------------------------------------------------------------
# 步骤2: 查看当前状态
# ------------------------------------------------------------------------------
echo -e "\n${YELLOW}【步骤2】查看当前状态...${NC}"
git status

# ------------------------------------------------------------------------------
# 步骤3: 拉取最新代码
# ------------------------------------------------------------------------------
echo -e "\n${YELLOW}【步骤3】拉取远程最新代码...${NC}"
git pull origin "${BRANCH}"

if [ $? -ne 0 ]; then
    echo -e "${RED}✗ 拉取代码失败，可能存在冲突！${NC}"
    exit 1
fi
echo -e "    ${GREEN}✓ 拉取成功${NC}"

# ------------------------------------------------------------------------------
# 步骤4: 添加所有更改
# ------------------------------------------------------------------------------
echo -e "\n${YELLOW}【步骤4】添加所有更改...${NC}"
git add .
echo -e "    ${GREEN}✓ 添加成功${NC}"

# ------------------------------------------------------------------------------
# 步骤5: 提交代码
# ------------------------------------------------------------------------------
echo -e "\n${YELLOW}【步骤5】提交代码...${NC}"
echo "    提交信息: ${COMMIT_MESSAGE}"
git commit -m "${COMMIT_MESSAGE}"

if [ $? -ne 0 ]; then
    echo -e "${RED}✗ 提交失败！${NC}"
    exit 1
fi
echo -e "    ${GREEN}✓ 提交成功${NC}"

# ------------------------------------------------------------------------------
# 步骤6: 推送到 GitHub
# ------------------------------------------------------------------------------
echo -e "\n${YELLOW}【步骤6】推送到 GitHub...${NC}"
git push origin "${BRANCH}"

if [ $? -ne 0 ]; then
    echo -e "${RED}✗ 推送失败！${NC}"
    exit 1
fi
echo -e "    ${GREEN}✓ 推送成功${NC}"

# ------------------------------------------------------------------------------
# 完成提示
# ------------------------------------------------------------------------------
echo -e "\n${GREEN}===============================================${NC}"
echo -e "${GREEN}              代码提交完成！${NC}"
echo -e "${GREEN}===============================================${NC}"
echo -e "\n${BLUE}提交信息:${NC} ${COMMIT_MESSAGE}"
echo -e "${BLUE}分支:${NC} ${BRANCH}"
echo -e "${BLUE}仓库:${NC} $(git remote get-url origin)"
echo -e "\n${GREEN}操作完成！${NC}"
echo -e "${GREEN}===============================================${NC}"
